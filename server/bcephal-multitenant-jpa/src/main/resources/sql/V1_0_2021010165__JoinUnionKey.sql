    id BIGINT not null,
	joinId BIGINT,
	position INTEGER,
	dimensionType VARCHAR(100),
	gridType VARCHAR(100),
	gridId BIGINT,
	columnId BIGINT,
	PRIMARY KEY (id)
);
CREATE SEQUENCE IF NOT EXISTS join_union_key_seq START WITH 1 INCREMENT BY 1;

