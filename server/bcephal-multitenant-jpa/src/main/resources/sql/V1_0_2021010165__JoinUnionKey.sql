CREATE TABLE IF NOT EXISTS BCP_JOIN_UNION_KEY_ITEM (
    id BIGINT not null,
	unionId BIGINT,
	position INTEGER,
	dimensionType VARCHAR(100),
	gridType VARCHAR(100),
	gridId BIGINT,
	columnId BIGINT,
	PRIMARY KEY (id)
);
